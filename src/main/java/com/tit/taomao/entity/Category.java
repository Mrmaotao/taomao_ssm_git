package com.tit.taomao.entity;

import org.springframework.stereotype.Controller;

@Controller
public class Category {
	int id;
	String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
